<?php

namespace MathHamzah;

class Calculator
{
    public function tambah($a, $b)
    {
        return $a + $b;
    }

    public function tolak($a, $b)
    {
        return $a - $b;
    }

    public function bahagi($a, $b)
    {
        return $a / $b;
    }

    public function darab($a, $b)
    {
        return $a * $b;
    }
}
